        private static void Add(int id, string name, int typeCode, string typeName, string category, string subCategory)
        {
            string jpName = name;
            string cnName = name;
            if (_cnNameMap.TryGetValue(name, out var mappedCn))
            {
                cnName = mappedCn;
            }

            if (_datNames != null && _datNames.TryGetValue(id, out var datName) && !string.IsNullOrEmpty(datName))
            {
                jpName = datName;
                if (!_cnNameMap.TryGetValue(datName, out cnName))
                {
                    cnName = datName;
                }
            }

            if (_datFullData != null && _datFullData.TryGetValue(id, out var fields)
                && fields.TryGetValue("ptr4", out var ptr4) && !string.IsNullOrEmpty(ptr4))
            {
                int colonIdx = ptr4.IndexOf('：');
                if (colonIdx < 0)
                    colonIdx = ptr4.IndexOf(':');
                if (colonIdx >= 0)
                {
                    category = ptr4.Substring(0, colonIdx).Trim();
                    subCategory = ptr4.Substring(colonIdx + 1).Trim();
                }
                else
                {
                    category = ptr4.Trim();
                }
            }

            _items[id] = new ItemData(id, jpName, cnName, typeCode, typeName, category, subCategory);
        }

        private static void Add(int id, string name, int typeCode, string typeName)
        {
            Add(id, name, typeCode, typeName, "", "");
        }
