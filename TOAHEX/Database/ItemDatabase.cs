        private static List<string> _datCategoryNames;

        public static void LoadFromDat(string filePath)
        {
            _datNames = DatDataLoader.LoadItemData(filePath);

            if (_initialized && _datNames != null)
            {
                foreach (var kv in _datNames)
                {
                    if (_items.TryGetValue(kv.Key, out var item) && !string.IsNullOrEmpty(kv.Value))
                    {
                        string cnName = kv.Value;
                        if (_cnNameMap.TryGetValue(kv.Value, out var mappedCn))
                            cnName = mappedCn;
                        var newItem = new ItemData(kv.Key, kv.Value, cnName, item.TypeCode, item.TypeName, item.Category, item.SubCategory);
                        _items[kv.Key] = newItem;
                    }
                }
            }
        }

        public static void LoadFromDatFull(string filePath)
        {
            _datFullData = DatDataLoader.LoadItemFullData(filePath);

            if (_datFullData == null) return;

            _datNames = new Dictionary<int, string>();
            _datCategoryNames = new List<string> { "全部" };
            var categorySet = new HashSet<string>();

            foreach (var kv in _datFullData)
            {
                if (kv.Value.TryGetValue("name", out var name) && !string.IsNullOrEmpty(name))
                {
                    _datNames[kv.Key] = name;
                }

                if (kv.Value.TryGetValue("ptr4", out var ptr4) && !string.IsNullOrEmpty(ptr4))
                {
                    string category = "";
                    string subCategory = "";
                    int colonIdx = ptr4.IndexOf('：');
                    if (colonIdx < 0)
                        colonIdx = ptr4.IndexOf(':');

                    if (colonIdx >= 0)
                    {
                        category = ptr4.Substring(0, colonIdx).Trim();
                        subCategory = ptr4.Substring(colonIdx + 1).Trim();
                    }
                    else
                    {
                        category = ptr4.Trim();
                    }

                    if (!string.IsNullOrEmpty(category) && !categorySet.Contains(category))
                    {
                        categorySet.Add(category);
                        _datCategoryNames.Add(category);
                    }

                    if (_initialized && _items.TryGetValue(kv.Key, out var item))
                    {
                        string jpName = item.JpName;
                        string cnName = item.CnName;
                        if (_datNames.TryGetValue(kv.Key, out var datName) && !string.IsNullOrEmpty(datName))
                        {
                            jpName = datName;
                            if (!_cnNameMap.TryGetValue(datName, out cnName))
                                cnName = datName;
                        }

                        var newItem = new ItemData(kv.Key, jpName, cnName, item.TypeCode, item.TypeName, category, subCategory);
                        _items[kv.Key] = newItem;
                    }
                }
            }

            if (_datCategoryNames.Count <= 1)
            {
                _datCategoryNames = null;
            }
        }

        public static IReadOnlyDictionary<int, ItemData> Items
        {
            get
            {
                EnsureInitialized();
                return _items;
            }
        }

        public static ItemData GetById(int id)
        {
            EnsureInitialized();
            return _items.TryGetValue(id, out var item) ? item : null;
        }

        public static List<ItemData> GetByCategory(string categoryName)
        {
            EnsureInitialized();
            if (categoryName == "全部") return _items.Values.ToList();

            return _items.Values.Where(i =>
            {
                if (i.Category == categoryName) return true;
                if (i.TypeName == categoryName) return true;
                if (!string.IsNullOrEmpty(i.Category) && !string.IsNullOrEmpty(i.SubCategory)
                    && (i.Category + "：" + i.SubCategory) == categoryName) return true;
                return false;
            }).ToList();
        }

        public static List<ItemData> GetByCategoryAndSubCategory(string category, string[] subCategories)
        {
            EnsureInitialized();
            if (subCategories == null || subCategories.Length == 0)
            {
                return _items.Values.Where(i => i.Category == category || i.TypeName == category).ToList();
            }

            var subSet = new HashSet<string>(subCategories);
            return _items.Values.Where(i =>
            {
                if (i.Category != category && i.TypeName != category) return false;
                if (string.IsNullOrEmpty(i.SubCategory)) return false;
                return subSet.Contains(i.SubCategory);
            }).ToList();
        }

        public static List<ItemData> GetByTypeCodes(int[] typeCodes)
        {
            EnsureInitialized();
            if (typeCodes == null || typeCodes.Length == 0) return new List<ItemData>();
            var codeSet = new HashSet<int>(typeCodes);
            return _items.Values.Where(i => codeSet.Contains(i.TypeCode)).ToList();
        }

