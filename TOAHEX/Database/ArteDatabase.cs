            return _charArteIds[charIndex].Length;
        }

        public static string GetArteName(int charIndex, int localIndex)
        {
            if (charIndex < 0 || charIndex >= _charArteIds.Length) return "???";
            var ids = _charArteIds[charIndex];
            if (localIndex < 0 || localIndex >= ids.Length) return "???";
            return GetName(ids[localIndex]);
        }

        public static List<string> GetArteNames(int charIndex)
        {
            if (charIndex < 0 || charIndex >= _charArteIds.Length) return new List<string>();
            var ids = _charArteIds[charIndex];
            var names = new List<string>(ids.Length);
            for (int i = 0; i < ids.Length; i++)
                names.Add(string.Format("{0}: {1}", i + 1, GetName(ids[i])));
            return names;
        }

        public static List<(int id, string jp, string cn)> GetAll()
        {
            return _data.Value.Select(kv => (kv.Key, kv.Value.jp, kv.Value.cn)).ToList();
        }

        public static List<(int id, string jp, string cn)> Search(string keyword)
        {
            if (string.IsNullOrEmpty(keyword)) return GetAll();
            var kw = keyword.ToLowerInvariant();
            return _data.Value
                .Where(kv => kv.Value.jp.ToLowerInvariant().Contains(kw) || kv.Value.cn.ToLowerInvariant().Contains(kw))
                .Select(kv => (kv.Key, kv.Value.jp, kv.Value.cn))
                .ToList();
        }
